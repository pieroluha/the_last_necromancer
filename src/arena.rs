fn setup_arena(image_handles: Res<ImageHandles>, mut commands: Commands) {
    let transform = Transform::from_xyz(ARENA_OFFSET, ARENA_OFFSET, 0.0);
    commands
        .spawn_bundle(SpriteBundle {
            texture: image_handles.arena.clone(),
            transform,
            ..default()
        })
        .insert(Name::new("Arena"))
        .with_children(|p| {
            let transform = Transform::from_xyz(0.0, 0.0, 3.0);
            p.spawn_bundle(SpriteBundle {
                texture: image_handles.arena_shadow.clone(),
                transform,
                ..default()
            });
        });
}

pub fn map_index(x: i32, y: i32) -> usize {
    ((y * ARENA_SIZE as i32) + x) as usize
}

pub struct Map {
    pub grid: [u8; ARENA_GRID_SIZE],
}

impl Default for Map {
    fn default() -> Self {
        Self { grid: ARENA }
    }
}

impl Map {
    pub fn in_bounds(&self, x: i32, y: i32) -> bool {
        if x >= 5
            && x < ARENA_SIZE as i32 - 3
            && y >= 5
            && y < ARENA_SIZE as i32 - 5
            && self.can_enter(x, y)
        {
            true
        } else {
            false
        }
    }

    pub fn can_enter(&self, x: i32, y: i32) -> bool {
        if self.grid[map_index(x, y)] == 0 {
            true
        } else {
            false
        }
    }
}

pub struct ArenaPlugin;
impl Plugin for ArenaPlugin {
    fn build(&self, app: &mut App) {
        app.init_resource::<Map>()
            .add_system_set(SystemSet::on_enter(Playing).with_system(setup_arena));
    }
}
